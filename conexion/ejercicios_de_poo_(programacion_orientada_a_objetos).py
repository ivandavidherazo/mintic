class Coche:
  def __init__(self, marca, modelo, velocidad):
    self.marca = marca
    self.modelo = modelo
    self.velocidad = velocidad
  def acelerar(self, aumento):
    self.velocidad += aumento
    print(f"El coche{self.marca} {self.modelo} aceleró. Nueva velocidad: {self.velocidad} km/h")

#creacion de un objeto de tipo de coche
mi_coche = Coche("Toyota", "Land Cruiser", 0)

#acelerar el coche
mi_coche.acelerar(20)
mi_coche.acelerar(30)