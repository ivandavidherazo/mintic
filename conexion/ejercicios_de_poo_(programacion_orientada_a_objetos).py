class MiClase:
  #Atributos
  atributo1 = "Valor1"
  atributo2 = 0

  #Metodos
  def metodo1(self):
    print("Hola desde el metodo 1!")

  def metodo2(self, parametro):
    self.atributo2 = parametro
    print(f"El atributo2 se actualizo a: (self.atributo2)")

#creacion de objetos a partir de la clase
objeto1 = MiClase()
objeto2 = MiClase()

#acceso a atributos y llamada a metodos
print(objeto1.atributo1)      #salida valor1

objeto1.metodo1()             #salida ¡hola desde el metodo 1!

objeto2.metodo2(42) #salida el atributo2 se actualizo a: 42
print(objeto2.atributo2) #salida 42

